typealias BootstrapDefault = Bootstrap.Default

extension Bootstrap {
    enum Default {
        // Breakpoints
        static let smallBreakpoint: LengthUnit = .px(576)
        static let mediumBreakpoint: LengthUnit = .px(768)
        static let largeBreakpoint: LengthUnit = .px(992)
        static let xLargeBreakpoint: LengthUnit = .px(1200)
        static let xxLargeBreakpoint: LengthUnit = .px(1400)

        // Container sizes
        static let smallContainer: LengthUnit = .px(540)
        static let mediumContainer: LengthUnit = .px(720)
        static let largeContainer: LengthUnit = .px(960)
        static let xLargeContainer: LengthUnit = .px(1140)
        static let xxLargeContainer: LengthUnit = .px(1320)
    }
}
